        <li> Việc thanh toán đặt cược sẽ dựa trên kết quả được công bố bởi NBA hoặc các cơ quan chính thức đặc biệt và mọi dữ liệu được thay đổi sau khi trò chơi sẽ bị coi là không hợp lệ. </li>
