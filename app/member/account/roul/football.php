    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn vì bất kỳ lý do nào trong nửa đầu mùa giải, tất cả các cược đặt trước sẽ bị hủy. Kết quả rõ ràng trừ khi sự kiện bị hủy hoặc bị gián đoạn. </li>
