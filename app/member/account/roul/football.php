<div id = "Quy tắc chung" class = "acc_rulesSoccer_Title"> Quy tắc chung </div>
<ul>
    <li> Trừ khi có quy định khác, tất cả cược đặt cược sẽ được giải quyết 90 phút trong thời gian được chỉ định trong trò chơi. </li>
    <li> 90 phút cuối cùng của trò chơi bao gồm thời gian chấn thương của người chơi. Làm thêm giờ, knockout, đá phạt, và bất kỳ thay đổi nào được thực hiện cho trọng tài hoặc cơ quan quản lý có liên quan sau khi sự kiện sẽ không được tính. </li>
    <li> Trừ khi được quy định khác trong các quy tắc chơi cá nhân, việc thanh toán tất cả các cược đặt cược sẽ được dựa trên kết quả 90 phút. </li>
    <li> Đối với một số sự kiện hoặc trận đấu giao hữu với thời gian cuối cùng là 80 phút (2 x 40 phút), tất cả mọi phiên cược sẽ được giải quyết dựa trên thời gian của trận đấu. </li>
    <li> Nếu thời gian cuối cùng của trận đấu Junior và Friendly là 70 phút (2 x 35 phút) hoặc ít hơn, công ty sẽ đưa ra thông báo trước khi bắt đầu cuộc thi, nếu không vé cho sự kiện này sẽ bị vô hiệu. </li>
    <li> Nếu sự kiện bị gián đoạn hoặc bị trì hoãn và không khởi động lại trong vòng 36 giờ, mọi cược đặt cho sự kiện đó sẽ bị coi là không hợp lệ và bị hủy trừ khi được quy định khác trong quy tắc đặt cược cá nhân. </li>
    <li> Nếu sự kiện được xác nhận là bị hủy, tất cả các cược trên sự kiện đó sẽ bị coi là không hợp lệ và bị hủy trừ khi được quy định khác trong quy tắc đặt cược cá nhân. Nếu trận đấu bị gián đoạn trong hiệp một, tất cả các cược đầu tiên sẽ bị hủy. Nếu trận đấu bị gián đoạn trong hiệp hai, tất cả các cược đặt cược đầu tiên sẽ vẫn hợp lệ, nhưng tất cả các cược đặt cược thứ hai sẽ bị hủy trừ khi có quy định khác trong quy tắc chơi cá nhân. </li>
    <li> Sở hữu bóng sẽ được tính trừ khi có quy định khác trong quy tắc chơi cá nhân. </li>
    <Li> Nếu các trò chơi hoặc sự kiện hủy, gián đoạn hoặc chậm trễ và không xác định tiếp tục trong vòng 36 giờ sau khi thời gian trận đấu bắt đầu chính thức, tất cả các cược đua sẽ được coi là không hợp lệ và bị hủy bỏ, trừ trường hợp quy định tại các quy tắc trong thể thao cá nhân Lưu ý. Một số đơn đặt hàng bị vô điều kiện chấm dứt sẽ được giải quyết cho phù hợp. Thủ tục thanh toán cho các thị trường như vậy được mô tả trong các quy tắc thể thao riêng biệt. Kết quả của công ty hủy bỏ tất cả các phiên cược trong sự kiện được coi là cuối cùng và không cần phải tham khảo trọng tài chính thức hoặc quyết định của bộ phận liên quan. Một loạt các cược sẽ tiếp tục được thanh toán theo kết quả của cược còn lại, và tỷ lệ cược cho việc hủy sẽ được tính là 1. </li>
    <li> Đối với các sự kiện quốc tế, tất cả mọi phiên cược sẽ vẫn còn hiệu lực miễn là địa điểm đã thay đổi vẫn ở trong cùng một quốc gia. </li>
    <li> Đối với các sự kiện quốc tế, tất cả mọi phiên cược sẽ vẫn còn hiệu lực miễn là địa điểm thay đổi vẫn còn ở quốc gia mà giải đấu ban đầu được lên lịch. </li>
    <li> Công ty bảo lưu quyền hủy bất kỳ cược nào có thể ảnh hưởng đến kết quả của sự kiện do thay đổi trang web. </li>
</ul>
