<h3> Đĩa kích thước </h3>
<h3> Quy tắc chung </h3>
<ul>
    <li> Tổng số bàn thắng ghi được trong trận đấu sẽ lớn hơn hoặc ít hơn số lượng quả bóng lớn / nhỏ quy định tại điểm chấp. </li>
    <li> Nếu tổng số bàn thắng ghi được trong sự kiện lớn hơn kích thước của điểm chấp Số lượng quả bóng là thị trường. Nếu nó nhỏ hơn số lượng quả bóng lớn / nhỏ trong cược chấp, nó là một quả bóng nhỏ. </li>
    <li> Tất cả mọi phiên cược sẽ được giải quyết bằng số quả bóng lớn / nhỏ được chơi bởi điểm chấp ở cuối trận đấu. </li>
    <li> Gameplay lớn / nhỏ được chia thành các danh mục sau: </li>
    <ul>
