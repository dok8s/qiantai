        <li> Lớn / nhỏ hơn 'bán cầu' (ví dụ: 1,5, 2,5, 3,5, v.v.) </li>
        <li> Kết hợp cách 'bán cầu / một quả bóng' ở trên (ví dụ: 1.5 / 2, 2.5 / 3, 3.5 / 4, v.v.) </li>
