<h2> Hãy để bóng </h2>
<h3> Quy tắc chung </h3>
<ul>
    <li> Dự đoán đội nào sẽ giành chiến thắng trong trận đấu ở tỷ lệ / toàn bộ / cuộc đua. </li>
    <li> & ldquo; Để bóng được xác định là điểm số ảo của một đội đã được bên kia đưa ra trước khi bắt đầu trò chơi chính thức, bắt đầu với sự dẫn đầu. </li>
    <li> Tất cả mọi phiên cược sẽ được giải quyết bằng điểm chấp khi số lần phát được phát ở cuối trận đấu. </li>
    <li> Để cho đội (đội chiếm ưu thế) đưa ra điểm chấp, số quả bóng sẽ được hiển thị ở phía bên trái của tỷ lệ cược, và đội bóng cũng sẽ hiển thị bằng chữ in đậm trên đĩa </li>
    <li> Cách chơi bóng được chia thành các danh mục sau: </li>
    <ul>
        <li> Số nguyên cho phép bóng cũng được gọi là let & lsquo; một quả bóng (ví dụ: -1, -2, -3, v.v.) </li>
        <li> Không phải số nguyên cho phép bóng cũng được gọi là & lsquo; bán cầu & rsquo; (ví dụ: -0,5, -1,5, -2,5, v.v.) </li>
        <li> Kết hợp cách trên & lsquo; bán cầu / một quả bóng & rsquo; (ví dụ: -0 / 0.5, -0.5 / 1, -1 / 1.5, v.v.) </li>
    </ul>
</ul>
<h3> Hãy để bóng </h3>
<ul>
    <li> Dựa trên điểm chấp để cho thông tin bóng dự đoán đội bóng cuối cùng sẽ thắng. </li>
    <li> Việc thanh toán các cược sẽ được dựa trên 90 phút của thời gian cuối cùng của trò chơi. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn trước khi kết thúc 90 phút, tất cả các phiên cược sẽ bị coi là không hợp lệ. </li>
</ul>
<h3> Đưa bóng - nửa đầu </h3>
<ul>
    <li> Tất cả các cược đặt trong hiệp một sẽ là 45 phút trong hiệp một, kể cả kết quả của chấn thương sau thời gian chấn thương. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn vì bất kỳ lý do nào trong nửa đầu mùa giải, tất cả các cược đặt trước sẽ bị hủy. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn trong hiệp hai hoặc ngoài giờ vì bất kỳ lý do gì, tất cả các cược đặt cược đầu tiên vẫn hợp lệ. </li>
</ul>
<h3> Bóng ròng rọc vào bóng </h3>
<ul>
    <li> Tất cả các cược sẽ được phát hành theo tỷ lệ chấp và sẽ được giải quyết sau khi loại cược tương ứng kết thúc. </li>
    <li> Dàn xếp dựa trên kết quả của cược vào cuối trận đấu / thời gian. Tức là, số điểm đặt cược tại thời điểm trận đấu được trừ từ số điểm của trận đấu. Nửa đầu của trò chơi đặt cược vào bóng sau khi kết thúc hiệp một. </li> </ul>
<h3> Làm thêm giờ - Để bóng </h3>
<ul>
    <li> Tất cả các cược sẽ được phát hành theo tỷ lệ chấp, được tính sau khi kết thúc thời gian làm thêm 30 phút, bao gồm cả thời gian bù giờ. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn trước khi kết thúc thời gian làm thêm, tất cả mọi phiên cược sẽ bị hủy. </li>
</ul>
<h3> Làm thêm giờ - Tỷ lệ chấp - nửa đầu </h3>
<ul>
    <li> Tất cả các cược sẽ được phát hành theo tỷ lệ chấp, được tính sau 15 phút làm thêm giờ, bao gồm cả thời gian bù giờ. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn trong nửa đầu của trận đấu khi làm thêm giờ, tất cả các cược đặt cược đầu tiên sẽ bị hủy. </li>
    <li> Nếu sự kiện bị hủy hoặc bị gián đoạn trong hiệp hai hoặc thời gian bù giờ làm thêm giờ, tất cả các cược đặt cược đầu tiên sẽ được coi là hợp lệ. </li>
</ul>
<h3> 15 phút (để bóng) </h3>
