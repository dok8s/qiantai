<h1> Giải bóng bầu dục / Giải bóng bầu dục </h1>
<h2> Quy tắc chung </h2>
<ul>
    <li> Nếu địa điểm thay đổi, tất cả mọi phiên cược sẽ bị hủy. </li>
    <li> Trừ khi có ghi chú khác, việc thanh toán tất cả các cược bóng bầu dục và bóng bầu dục sẽ dựa trên kết quả của thời gian kết thúc 80 phút. </li>
    <li> Thời gian kết thúc trận đấu ‘80 phút ’bao gồm bất kỳ thời gian chấn thương nào. </li>
    <li> Tất cả các trò chơi không bao gồm thêm giờ, trừ khi có quy định khác. </li>
    <li> Việc thanh toán tất cả các cược đặt cược sẽ dựa trên kết quả của thời gian kết thúc 80 phút. </li>
    <li> Đối với các sự kiện quốc tế, tất cả mọi phiên cược sẽ vẫn còn hiệu lực miễn là địa điểm đã thay đổi vẫn ở trong cùng một quốc gia. </li>
    <li> Tất cả các cược nửa đầu sẽ được giải quyết sau khi kết thúc hiệp một. </li>
    <li> Phần thứ hai của cược sẽ được giải quyết trong hiệp hai (bao gồm cả thời gian bù giờ hoặc thời gian bù giờ). </li>
    <li> Nếu trận đấu bị gián đoạn trong hiệp một, tất cả cược đặt trước sẽ bị hủy. Nếu trận đấu bị gián đoạn trong hiệp hai hoặc hiệp phụ, tất cả các cược nửa đầu sẽ vẫn còn hiệu lực, nhưng tất cả các cược đặt cược thứ hai sẽ bị hủy. </li>
</ul>

<h2> Loại đặt cược </h2>

<ul class = "acc_backUL">
    <h3> Dự báo vô địch (hạ cấp tiền hoàn lại) </h3>
    <ul>
        <li> Dự đoán đội nào sẽ giành chiến thắng trong trò chơi ở cuối trò chơi. </li>
        <li> Nếu kết quả của trận đấu là hòa sau khi kết thúc trận đấu, tất cả mọi phiên cược sẽ bị hủy. </li>
    </ul>

    <h3> Dự báo về nhà vô địch (bao gồm cả làm thêm giờ) </h3>
    <ul>
        <li> Dự đoán đội nào sẽ thắng trong trò chơi. </li>
        <li> Kết quả của trận đấu và trận đấu thêm giờ áp dụng. </li>
        <li> Nếu trận hòa được thực hiện sau thời gian kết thúc và giải quyết thêm giờ, tất cả các cược vào dự đoán của nhà vô địch (bao gồm cả làm thêm giờ) sẽ bị hủy. </li>
    </ul>

    <h3> Chiến thắng </h3>
    <ul>
        <li> Dự đoán đội nào sẽ thắng hoặc hòa vào cuối trận đấu. </li> Hai đội và Văn phòng Hòa bình được cung cấp làm tùy chọn đặt cược. </li>
</ul>

<h3> Chiến thắng một mình - nửa đầu </h3>
<ul>
    <li> Dự đoán đội nào sẽ thắng hoặc hòa trong hiệp một. </li> Hai đội và Văn phòng Hòa bình được cung cấp làm tùy chọn đặt cược. </li>
</ul>

<h3> Chiến thắng một mình - nửa thứ hai </h3>
<ul>
    <li> Dự đoán đội nào sẽ thắng hoặc hòa vào hiệp hai. </li> Hai đội và Văn phòng Hòa bình được cung cấp làm tùy chọn đặt cược. </li>
</ul>

<h3> Đưa bóng </h3>
<ul>
    <li> Dự đoán đội nào sẽ giành chiến thắng trong một số trận đấu được chỉ định. </li>
    <li> Giải quyết là điểm số cuối cùng sau khi kết thúc trận đấu, bao gồm cả số điểm tại thời điểm đặt cược. </li>
</ul>

<h3> Đưa bóng - nửa đầu </h3>
<ul>
    <li> Dự đoán đội nào sẽ giành chiến thắng trong nửa đầu của trò chơi. </li>
    <li> Giải quyết là điểm số cuối cùng sau khi kết thúc hiệp một, bao gồm cả số điểm tại thời điểm đặt cược. </li>
</ul>

<h3> Tặng bóng (kể cả giờ làm thêm) </h3>
<ul>
    <li> Dự đoán đội nào sẽ thắng trong trò chơi. </li>
    <li> Kết quả của trận đấu và trận đấu thêm giờ áp dụng. </li>
    <li> Việc thanh toán dựa trên số điểm cuối cùng của thời gian làm thêm giờ (nếu được ghi), bao gồm cả số điểm hiện tại tại thời điểm đặt cược. </li>
</ul>

<h3> Tổng số điểm: Lớn / Nhỏ </h3>
<ul>
    <li> Dự đoán thời gian trận đấu, tổng số điểm của hai đội sẽ lớn hơn hoặc ít hơn số điểm lớn / nhỏ được chỉ định tại điểm chấp. </li>
    <li> Nếu sự kiện bị gián đoạn ở bất kỳ giai đoạn nào và không có trường hợp đáng kể nào ảnh hưởng đến kết quả của sự kiện, đặt cược lớn / nhỏ được coi là đặt cược không hợp lệ. </li>
</ul>

<h3> Tổng số điểm: Lớn / Nhỏ - Nửa đầu </h3>
<ul>
    <li> Dự đoán trận đấu trong hiệp một, tổng số điểm của hai đội sẽ lớn hơn hoặc ít hơn số điểm lớn / nhỏ quy định tại điểm chấp. </li>
    <li> Nếu sự kiện bị gián đoạn trong nửa đầu và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện, đặt cược lớn / nhỏ được coi là đặt cược không hợp lệ. </li>
</ul>

<h3> Tổng số điểm - 3 cược: </h3>
<ul>
    <li> Để dự đoán thời gian của trận đấu, tổng số điểm của hai đội sẽ lớn hơn hoặc nhỏ hơn hoặc trong số điểm của bộ lớn / nhỏ được xác định trong điểm chấp. </li>
    <li> Ví dụ: Nếu điểm số được cung cấp là 50-60, nếu điểm số cuối cùng là 49 hoặc ít hơn, "nhỏ" sẽ là lựa chọn thành công, nếu điểm số cuối cùng là 61 hoặc cao hơn, so với "chung là Một sự lựa chọn thành công, nếu khoảng thời gian đặt cược, và điểm số cuối cùng ở bất cứ đâu trong khoảng từ 50-60, sẽ là một vụ cá cược thành công. </li>
    <li> Nếu sự kiện bị gián đoạn ở bất kỳ giai đoạn nào và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện, tất cả các phiên cược được coi là không hợp lệ. </li>
</ul>

<h3> Điểm số của đội: lớn / nhỏ </h3>
<ul>
    <li> Đối với trận đấu dự đoán, tổng số điểm của đội được chỉ định sẽ lớn hơn hoặc ít hơn điểm chấp lớn / nhỏ được xác định bởi điểm chấp. </li>
    <li> Tất cả các cược lớn / nhỏ được coi là không hợp lệ nếu sự kiện bị gián đoạn ở bất kỳ giai đoạn nào và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện. </li>
</ul>

<h3> Điểm số của đội: lớn / nhỏ - nửa đầu </h3>
<ul>
    <li> Đối với trận đấu dự đoán, tổng số điểm của đội được chỉ định sẽ lớn hơn hoặc nhỏ hơn số điểm lớn / nhỏ được xác định bởi điểm chấp. </li>
    <li> Nếu sự kiện bị gián đoạn trong nửa đầu và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện, tất cả các cược lớn / nhỏ được coi là không hợp lệ. </li>
</ul>

<h3> Điểm số của đội chủ nhà: lớn / nhỏ - nửa đầu </h3>
<ul>
    <li> Dự đoán thời gian kết thúc của trận đấu trong hiệp một, số điểm của đội chủ nhà sẽ lớn hơn hoặc nhỏ hơn số điểm lớn / nhỏ được chỉ định bởi điểm chấp. </li>
    <li> Nếu sự kiện bị gián đoạn trong nửa đầu và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện, tất cả các cược lớn / nhỏ được coi là không hợp lệ. </li>
</ul>

<h3> Điểm số đội khách đi xa: lớn / nhỏ - nửa đầu </h3>
<ul>
    <li> Dự đoán trận đấu trong nửa đầu của trận đấu, số điểm của đội khách sẽ lớn hơn hoặc ít hơn số điểm lớn / nhỏ được chỉ định. </li>
    <li> Nếu sự kiện bị gián đoạn trong nửa đầu và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện, tất cả các cược lớn / nhỏ được coi là không hợp lệ. </li>
</ul>

<h3> Chạm vào </h3>
<ul>
    <li> Dự đoán đội nào trong trò chơi sẽ là đội tiếp theo chạm đất. </li>
    <li> Nếu trận đấu bị gián đoạn trước khi đội được chỉ định chạm đất, tất cả mọi phiên cược sẽ bị hủy. </li>
</ul>

<h3> Chạm vào </h3>
<ul>
    <li> Dự đoán đội nào trong trò chơi sẽ là đội cuối cùng ghi điểm. </li>
    <li> Nếu lệnh trọng tài chuyển sang nhóm tiếp xúc, kết quả của trận đấu sẽ được coi là chấp nhận được. </li>
    <li> Nếu vậy, tất cả mọi phiên cược sẽ được coi là không hợp lệ. </li>
</ul>


<h3> Tổng số điểm cảm ứng: Lớn / Nhỏ </h3>
<ul>
    <li> Đối với sự kiện được dự đoán, tổng số điểm của lần chạm sẽ lớn hơn hoặc nhỏ hơn số điểm lớn / nhỏ được chỉ định bởi điểm chấp. </li>
    <li> Tất cả các cược lớn / nhỏ được coi là không hợp lệ nếu sự kiện bị gián đoạn ở bất kỳ giai đoạn nào và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện. </li>
</ul>


<h3> Chạm vào (nhóm): Lớn / Nhỏ </h3>
<ul>
    <li> Đối với trận đấu dự đoán, tổng số điểm của touchdown (đội chủ nhà hoặc đội khách) sẽ lớn hơn hoặc nhỏ hơn số điểm lớn / nhỏ được xác định bởi điểm chấp. </li>
    <li> Tất cả các cược lớn / nhỏ được coi là không hợp lệ nếu sự kiện bị gián đoạn ở bất kỳ giai đoạn nào và không có sự kiện quan trọng nào ảnh hưởng đến kết quả của sự kiện. </li>
</ul>

<h3> Bảng cảm ứng của đội chủ nhà </h3>
<ul>
    <li> Dự đoán sự kiện liệu đội chủ nhà có ghi được một hoặc nhiều điểm trong bất kỳ thời gian hoàn thành nào không. </li>
</ul>

<h3> Touchdown của đội khách </h3>
<ul>
    <li> Dự đoán sự kiện liệu đội khách đã ghi được điểm hay nhiều hơn trong bất kỳ thời gian kết thúc nào. </li>
</ul>

<h3> Đội chủ nhà ghi được hai điểm trở lên </h3>
<ul>
    <li> Đối với sự kiện, đội chủ nhà sẽ ghi hai hoặc nhiều điểm vào bất kỳ thời điểm kết thúc nào. </li>
</ul>

<h3> Đội khách đã ghi được hai điểm trở lên </h3>
<ul>
    <li> Dự đoán sự kiện liệu đội khách đã ghi được hai hay nhiều điểm trong bất kỳ thời gian hoàn thành nào. </li>
</ul>

<h3> Đội chủ nhà ghi được ba điểm trở lên khi chạm vào </h3>
<ul>
    <li> Dự đoán sự kiện liệu đội chủ nhà có ghi được ba điểm trở lên trong bất kỳ thời gian kết thúc nào không. </li>
</ul>

<h3> Đội khách đã ghi được ba điểm trở lên </h3>
<ul>
    <li> Dự đoán sự kiện liệu đội khách đã giành được ba điểm trở lên trong bất kỳ thời gian hoàn thành nào hay không. </li>
</ul>

<h3> Điểm số thắng ròng () </h3>
<ul>
    <li> Dự đoán đội nào là đội chiến thắng ròng sau khi kết thúc trận đấu. </li>
    <li> Dàn xếp sẽ dựa trên điểm số cuối cùng của cả hai đội ở cuối trận đấu. </li>
    <li> 3 cách khác nhau có sẵn:
        <ul>
            <li> Dải Điểm Thay đổi, ví dụ: các điểm X-X. </li>
            <li> 10 Băng tần Điểm, ví dụ: 01-10, 11- 20, 21-30 vv. </li>
            <li> 5 Băng tần Điểm, ví dụ: 01-5, 6-10, 11-15, 16-20 vv. </li>
        </ul>
    </li>
    <li> I Nếu sự kiện bị gián đoạn bất kỳ lúc nào, tất cả các cược vào sự kiện đó sẽ bị coi là không hợp lệ và bị hủy. </li>
</ul>


<h3> Trình phát chạm đầu tiên </h3>
<ul>
    <li> Dự đoán người chơi đã ghi bàn thắng đầu tiên trong 80 phút của trận đấu. </li>
    <li> Nếu người chơi đặt cược tham gia vào trận đấu trước lần chạm đầu tiên, tất cả mọi phiên cược sẽ được coi là hợp lệ. </li>
    <li> Nếu người chơi đặt cược vào trò chơi sau lần chạm đầu tiên, tất cả mọi phiên cược sẽ bị hủy. </li>
    <li> Nếu người chơi cá cược không tham gia trận đấu, tất cả mọi phiên cược sẽ bị hủy. </li>
</ul>


<h3> Trình phát điểm số lần chạm cuối cùng </h3>
<ul>
    <li> Dự đoán người chơi đã ghi bàn thắng cuối cùng trong 80 phút của trận đấu. </li>
    <li> Nếu người chơi đặt cược tham gia vào trò chơi trong vòng 80 phút của trò chơi, tất cả mọi phiên cược sẽ được coi là hợp lệ. </li>
    <li> Nếu người chơi cá cược không tham gia trận đấu, tất cả mọi phiên cược sẽ bị hủy. </li>
</ul>


<h3> Trình ghi bàn chạm </h3>
<ul>
    <li> Dự đoán liệu người chơi đặt cược vào thời gian kết thúc 80 phút sẽ chạm đất. </li>
    <li> Nếu người chơi đặt cược tham gia vào trò chơi trong vòng 80 phút của trò chơi, tất cả mọi phiên cược sẽ được coi là hợp lệ. </li>
    <li> Nếu người chơi cá cược không tham gia trận đấu, tất cả mọi phiên cược sẽ bị hủy. </li>
    <li> Nếu trận đấu bị gián đoạn sau khi người chơi chạm đất, tất cả các cược trên trình phát chạm sẽ vẫn hợp lệ. </li>
</ul>


<h3> Nhóm cảm ứng đầu tiên </h3>
<ul>
    <li> Nhóm dự đoán lần chạm đầu tiên. </li>
    <li> Hình phạt sẽ không được tính. </li> Nếu một hình phạt được thực hiện, điểm tiếp theo được coi là hợp lệ. </li>
<li> Nếu trận đấu bị gián đoạn sau một điểm, tất cả các cược của đội bóng chạm đầu tiên sẽ vẫn hợp lệ. </li>
<li> Nếu trận đấu bị gián đoạn trước khi không có đội nào chạm vào điểm số, tất cả các cược của đội bóng chạm đầu tiên sẽ bị hủy. </li>
<li> Nếu trận đấu không có sự chạm trán của đội trong 80 phút của trận đấu và thời gian chấn thương, tất cả các cược của đội bóng chạm đầu tiên sẽ bị hủy. </li>
</ul>


<h3> Đội điểm số liên lạc cuối cùng </h3>
<ul>
    <li> Nhóm dự đoán lần chạm cuối cùng. </li>
    <li> Hình phạt sẽ không được tính. </li> Nếu một hình phạt được thực hiện, điểm tiếp theo được coi là hợp lệ. </li>
<li> Nếu trận đấu bị gián đoạn, tất cả mọi phiên cược trong đội cuối cùng sẽ bị hủy. </li>
<li> Nếu trận đấu không có sự chạm trán của đội trong 80 phút của trận đấu và thời gian chấn thương, tất cả mọi phiên cược trong đội cuối cùng sẽ bị hủy. </li>
</ul>


<h3> Đội ghi bàn đầu tiên </h3>
<ul>
    <li> Dự đoán đội đã ghi bàn lần đầu tiên trong trận đấu (bao gồm cả trò chơi hình phạt). </li>
    <li> Nếu trận đấu bị gián đoạn sau khi một đội bóng ghi bàn, tất cả các cược của đội ghi bàn đầu tiên sẽ vẫn hợp lệ. </li>
    <li> Nếu trận đấu bị gián đoạn trước khi điểm số của đội, tất cả các cược của đội ghi bàn đầu tiên sẽ bị hủy. </li>
    <li> Nếu sự kiện không ghi bàn trong 80 phút của trận đấu và thời gian chấn thương, tất cả các cược của đội đầu tiên sẽ bị hủy. </li>
</ul>


<h3> Đội điểm số cuối cùng </h3>
<ul>
    <li> Dự đoán đội ghi được vào cuối trận đấu (bao gồm cả trò chơi hình phạt). </li>
    <li> Nếu trận đấu bị gián đoạn, tất cả các cược vào đội ghi bàn cuối cùng sẽ bị hủy. </li>
    <li> Nếu trận đấu không ghi bàn trong vòng 80 phút của trận đấu và thời gian bù giờ, tất cả các cược từ đội cuối cùng sẽ bị hủy. </li>
</ul>
