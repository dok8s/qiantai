<h1>Cược vô địch</h1>
<p>Thị trường Champion được thiết kế để dự đoán liệu một giải đấu, giải đấu, giải đấu hay xe hơi có giành được chức vô địch hay không. Thị trường vô địch bao gồm các loại sau (không giới hạn ở đây):</p>
<ul>
    <li>Kết quả cuối cùng của một trận đấu, chẳng hạn như nhà vô địch World Cup hoặc F1 Drivers Championship.</li>
    <li>Kết quả cuối cùng của vòng bảng, chẳng hạn như chức vô địch nhóm World Cup.</li>
    <li>Kết quả cuối cùng của một sự kiện, ví dụ: một đội từ trận bán kết đến trận chung kết, bất kể số điểm, cho dù có thêm giờ hay bị phạt.</li>
    <li>Kết quả cuối cùng của chiếc xe, ví dụ: nhà vô địch cá nhân của sự kiện F1.</li>
    <li>Người ghi bàn cao nhất trong một trận đấu.</li>
    <li>Nhận những người chơi có giá trị nhất và hơn thế nữa.</li>
</ul>

<h2>Quy tắc chung</h2>
<ul>
    <li>Tất cả các khoản thanh toán cá cược vô địch đều phải chịu kết quả cuối cùng.</li>
    <li>Tất cả các cược vô địch được coi là hợp lệ bất kể người hoặc đội được chọn có tham gia hay không.</li>
    <li>Quy tắc thắng và thua áp dụng cho thị trường cá cược vô địch.</li>
    <li>Trong mọi trường hợp, người tham gia sử dụng "bất kỳ người chơi nào khác" hoặc "bất kỳ đội nào khác" thay cho tên sẽ được coi là vô danh.</li>
    <li>Nếu môn thể thao có quy tắc đặc biệt, nó sẽ thay thế các quy tắc vô địch chung.</li>
    <li>Người chiến thắng liên quan đến bất kỳ tháng cụ thể nào: Thị trường sẽ bị tạm ngưng vào lúc 23:59 (giờ Vương quốc Anh) vào ngày cuối cùng của tháng trước đó. Sự chậm trễ hoặc hủy bỏ sự kiện sẽ không ảnh hưởng đến kết quả. Nếu thị trường không đóng cửa do lỗi của con người hoặc thất bại kỹ thuật, công ty có quyền hủy bỏ tất cả các cược sử dụng đặt cược sai này.</li>
    <li>Cược vô địch chỉ có sẵn cho các mùa thông thường (trừ khi có quy định khác). Thứ hạng cuối cùng của đội sau khi kết thúc mùa giải được quyết định giành chức vô địch. Các giải đấu tương ứng trong playoffs hoặc các câu hỏi tiếp theo (điểm tiềm năng) sẽ không được tính trong kết quả cuối cùng.</li>
</ul>

