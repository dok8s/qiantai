<h1> trọng lượng nâng </h1>
<h2> Quy tắc chung </h2>
<ul>
    <li> Những người tham gia sẽ nhận được huy chương vàng, bạc và đồng như người chiến thắng đầu tiên, thứ hai và thứ ba. </li>
    <li> Kết quả của mỗi sự kiện được xếp hạng trên bục giảng là kết quả cuối cùng. Nếu sự không đủ tiêu chuẩn hoặc hiệu chỉnh kết quả xảy ra sau đó, nó sẽ không được tính toán. </li>
    <li> Khi đặt cược vào hai trận đấu, đặt cược sẽ chỉ có giá trị nếu cả hai người tham gia cuộc thi. </li>
    <li> Nếu trận đấu bắt đầu sớm, chỉ các cược đặt trước khi bắt đầu trận đấu mới được coi là cược hợp lệ. Cược đặt sau khi trận đấu bắt đầu sẽ bị hủy và loại cược không được bao gồm. </li>
    <li> Ngay cả khi lễ trao giải không được tổ chức trong sự kiện, kết quả của vụ cược sẽ dựa trên kết quả chính thức được công bố bởi Liên đoàn Quốc tế Quan hệ Quốc tế (IWF). </li>
</ul>